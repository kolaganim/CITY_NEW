package oops;

public class MInfo {
    private int age;
    private String name;
    private String branch;
    public int getAge() {

        return age;
    }
    public void setAge(int n){

        age=n;
    }
    public String getName(){

        return name;
    }
    public void setName(String name){

        this.name = name;
    }
    public String getBranch(){

        return branch;
    }
    public void setBranch(String branch){

        this.branch = branch;
    }
}
