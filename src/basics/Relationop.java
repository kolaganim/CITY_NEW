package basics;

public class Relationop {
    public static void main(String[] args) {
        int a = 100;
        int b = 40;
        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}